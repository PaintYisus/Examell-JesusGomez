    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        Empleado empleado;
        tblModel = new DefaultTableModel(null, columns);

        for (int i = 0; i < listaEmpleado.size(); i++) {
            empleado = listaEmpleado.get(i);
            if (empleado.getNombre().toLowerCase().contains(txtBuscar.getText().toLowerCase())) {
                Object row[] = {empleado.getCedula(), empleado.getNombre(),
                    empleado.getApellidos(), empleado.getSexo(), empleado.getFechaNac(), empleado.getEmail(), empleado.getSalario(), empleado.getPorcComision()};
                tblModel.addRow(row);
            }
        }
        tblRegistros.setModel(tblModel);
    }//GEN-LAST:event_txtBuscarKeyReleased

    private void mostrarTabla() {

        tblModel = new DefaultTableModel(null, columns);
        for (int i = 0; i < listaEmpleado.size(); i++) {
            empleado = listaEmpleado.get(i);

            Object row[] = {empleado.getCedula(), empleado.getNombre(),
                empleado.getApellidos(), empleado.getSexo(), empleado.getFechaNac(), empleado.getEmail(), empleado.getSalario(), empleado.getPorcComision()};
            tblModel.addRow(row);
        }
        tblRegistros.setModel(tblModel);
        txtCantidadRegistros.setText(String.valueOf(tblRegistros.getRowCount()));
    }

