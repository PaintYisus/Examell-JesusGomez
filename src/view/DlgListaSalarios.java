    private void mostrarTabla() {

        tblModel = new DefaultTableModel(null, columns);
        for (int i = 0; i < listaEmpleado.size(); i++) {
            empleado = listaEmpleado.get(i);
            double salario = empleado.getSalario() + (empleado.getSalario() * empleado.getPorcComision() / 100);
            Object row[] = {empleado.getCedula(), empleado.getNombre(),
                empleado.getApellidos(), empleado.getSexo(), empleado.getSalario(), empleado.getPorcComision(), salario};
            tblModel.addRow(row);
        }
        tblSalarios.setModel(tblModel);
    }

